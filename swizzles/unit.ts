interface Callback12 {
  (a: Expression<Vec2Primitive | Vec3Primitive | Vec4Primitive>): Expression<
    FloatPrimitive
  >;
  (a: Expression<Ivec2Primitive | Ivec3Primitive | Ivec4Primitive>): Expression<
    IntPrimitive
  >;
  (a: Expression<Bvec2Primitive | Bvec3Primitive | Bvec4Primitive>): Expression<
    BoolPrimitive
  >;
}

function forVec12(
  xyzwFactory: Callback12,
  xyzwName: string,
  rgbaFactory: Callback12,
  rgbaName: string,
  stpqFactory: Callback12,
  stpqName: string,
  indexA: number
): void {
  function overload(factory: Callback12, name: string): void {
    floatScenario(
      `${name} vec2`,
      { a: vec2(float(0.1843137254901961), float(0.5803921568627451)) },
      ({ a }) => factory(a),
      [47, 148][indexA] / 255
    );

    intScenario(
      `${name} ivec2`,
      { a: ivec2(int(47), int(148)) },
      ({ a }) => factory(a),
      [47, 148][indexA]
    );

    boolScenario(
      `${name} bvec2 true`,
      { a: bvec2(bool(indexA === 0), bool(indexA === 1)) },
      ({ a }) => factory(a),
      true
    );

    boolScenario(
      `${name} bvec2 false`,
      { a: bvec2(bool(indexA !== 0), bool(indexA !== 1)) },
      ({ a }) => factory(a),
      false
    );
  }

  forVec13(
    xyzwFactory,
    xyzwName,
    rgbaFactory,
    rgbaName,
    stpqFactory,
    stpqName,
    indexA
  );
  overload(xyzwFactory, xyzwName);
  overload(rgbaFactory, rgbaName);
  overload(stpqFactory, stpqName);
}

